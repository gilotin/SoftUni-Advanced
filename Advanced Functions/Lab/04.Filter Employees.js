function filter(data, criteria){
    criteria = criteria.split('-');
    let counter = 0;
    
    let arrData = JSON.parse(data);
    for(let el of arrData){
        if(el[criteria[0]] === criteria[1]){
            console.log(`${counter}. ${el.first_name} ${el.last_name} - ${el.email}`);
        counter++;    
        }
        
    }

}

